# Exercise 10: Use of escape character in a string

tabby_cat = "\tI'm tabbed in." # Escaping \t or adding a tab
persian_cat = "I'm split\non a line" # Escaping \n or adding next line
backslash_cat = "I'm \\ a \\ cat" # Escaping \\ or adding '\' in the string
# Use of three double-quotes with escape charaters 
fat_cat = '''
I'll do a list:
\t* Cat food
\t* Fishes
\t* Catnip\n\t* Grass
'''

# Printing all the variables:

print(tabby_cat)
